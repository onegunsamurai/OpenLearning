    def test_snake_case_keys_used(self):
        """Confirm formatter reads skill_id/skill_name, not skillId/skillName."""
        camel_scores = [{"skillId": "react", "skillName": "React", "score": 72}]
        md_camel = build_assessment_markdown(
            session_id="s1",
            target_level="mid",
            completed_at=None,
            knowledge_graph=None,
            gap_nodes=None,
            learning_plan=None,
            proficiency_scores=camel_scores,
        )
        # camelCase keys should NOT match — falls back to '—'
        assert "| — |" in md_camel

        snake_scores = [{"skill_id": "react", "skill_name": "React", "score": 72}]
        md_snake = build_assessment_markdown(
            session_id="s1",
            target_level="mid",
            completed_at=None,
            knowledge_graph=None,
            gap_nodes=None,
            learning_plan=None,
            proficiency_scores=snake_scores,
        )
        assert "| React |" in md_snake

