class TestAssessmentExportRoute:
    @pytest.mark.asyncio
    async def test_export_session_not_found_returns_404(self, setup_db):
        async with AsyncClient(
            transport=ASGITransport(app=_test_app), base_url="http://test"
        ) as client:
            response = await client.get("/api/assessment/nonexistent/export")
        assert response.status_code == 404

    @pytest.mark.asyncio
    async def test_export_from_db_result_returns_200_markdown(self, setup_db):
        async with _TestSessionFactory() as db:
            await seed_session(db, status="completed")
            await seed_result(db)

        async with AsyncClient(
            transport=ASGITransport(app=_test_app), base_url="http://test"
        ) as client:
            response = await client.get("/api/assessment/sess-001/export")

        assert response.status_code == 200
        assert "# Assessment Report" in response.text

    @pytest.mark.asyncio
    async def test_export_response_content_type_is_text_markdown(self, setup_db):
        async with _TestSessionFactory() as db:
            await seed_session(db, status="completed")
            await seed_result(db)

        async with AsyncClient(
            transport=ASGITransport(app=_test_app), base_url="http://test"
        ) as client:
            response = await client.get("/api/assessment/sess-001/export")

        assert "text/markdown" in response.headers["content-type"]

    @pytest.mark.asyncio
    async def test_export_response_has_content_disposition_attachment(self, setup_db):
        async with _TestSessionFactory() as db:
            await seed_session(db, status="completed")
            await seed_result(db)

        async with AsyncClient(
            transport=ASGITransport(app=_test_app), base_url="http://test"
        ) as client:
            response = await client.get("/api/assessment/sess-001/export")

        assert "attachment" in response.headers["content-disposition"]
        assert "sess-001"[:8] in response.headers["content-disposition"]

    @pytest.mark.asyncio
    async def test_export_response_has_content_length(self, setup_db):
        async with _TestSessionFactory() as db:
            await seed_session(db, status="completed")
            await seed_result(db)

        async with AsyncClient(
            transport=ASGITransport(app=_test_app), base_url="http://test"
        ) as client:
            response = await client.get("/api/assessment/sess-001/export")

        assert "content-length" in response.headers
        assert int(response.headers["content-length"]) == len(response.content)

    @pytest.mark.asyncio
    async def test_export_falls_back_to_graph_state_when_no_db_row(self, setup_db):
        async with _TestSessionFactory() as db:
            await seed_session(db, status="completed")
            # No AssessmentResult row — must fall back to graph state

        fake_kg = MagicMock()
        fake_kg.model_dump.return_value = FULL_KNOWLEDGE_GRAPH
        fake_kg.nodes = []

        fake_state_values = {
            "knowledge_graph": fake_kg,
            "gap_nodes": [],
            "learning_plan": None,
        }
        mock_graph_state = MagicMock()
        mock_graph_state.values = fake_state_values
        _mock_graph.aget_state = AsyncMock(return_value=mock_graph_state)

        try:
            async with AsyncClient(
                transport=ASGITransport(app=_test_app), base_url="http://test"
            ) as client:
                response = await client.get("/api/assessment/sess-001/export")

            assert response.status_code == 200
            assert "# Assessment Report" in response.text
        finally:
            _mock_graph.reset_mock()

    @pytest.mark.asyncio
    async def test_export_incomplete_assessment_returns_200_with_fallbacks(self, setup_db):
        async with _TestSessionFactory() as db:
            await seed_session(db, status="completed")
            result = AssessmentResult(
                session_id="sess-001",
                knowledge_graph=None,
                gap_nodes=None,
                learning_plan=None,
                proficiency_scores=None,
            )
            db.add(result)
            await db.commit()

        async with AsyncClient(
            transport=ASGITransport(app=_test_app), base_url="http://test"
        ) as client:
            response = await client.get("/api/assessment/sess-001/export")

        assert response.status_code == 200
        assert "*No gaps identified.*" in response.text
        assert "*Learning plan not yet generated.*" in response.text
